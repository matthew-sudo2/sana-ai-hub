/**
 * API client for Sana AI Hub backend.
 * Handles all HTTP communication with the FastAPI server.
 */

const API_BASE_URL = import.meta.env.VITE_API_BASE_URL || "http://localhost:8000";

export interface RunStatus {
  run_id: string;
  phase: string;
  status: string;
  error: string | null;
  output_dir: string | null;
}

export interface ImagesList {
  images: string[];
  count: number;
}

export interface ReportData {
  content: string;
  filename: string;
}

export interface ColumnStats {
  column: string;
  standard_deviation: number;
  max: number;
  min: number;
  variance: number;
  range: number;
  median: number;
  mode: number | null;
}

export interface StatsResponse {
  run_id: string;
  rows: number;
  columns: number;
  numeric_columns: number;
  stats: ColumnStats[];
}

export interface CustomChartResponse {
  run_id: string;
  filename: string;
  chart_type: string;
  columns_used: string[];
  instruction: string;
}

// ---------------------------------------------------------------------------
// Chart Explanation types
// ---------------------------------------------------------------------------

/** Per-chart statistics extracted from the Analyst Agent output. */
export interface ChartStats {
  mean?: number | null;
  median?: number | null;
  std?: number | null;
  min?: number | null;
  max?: number | null;
  outlier_count?: number | null;
  trend?: string | null;
  missing_count?: number | null;
  missing_pct?: number | null;
}

/**
 * Plain-English explanation for a single chart, generated deterministically
 * by the Artist Agent using column statistics and correlation data.
 */
export interface ChartExplanation {
  /** PNG filename, e.g. "hist_age.png" */
  filename: string;
  /** Internal ID, e.g. "hist_age" */
  chart_id: string;
  /** "histogram" | "scatter" | "bar" | "pie" | "line" | "box" | "heatmap" | "bespoke" */
  chart_type: string;
  /** Column names used to generate this chart */
  columns_used: string[];
  /** Human-readable chart title */
  title: string;
  /** "builtin" | "llm" | "custom_instruction" */
  generated_by: string;
  /** Plain-English explanation sentence(s) */
  explanation: string;
  /** Relevant numeric stats for the primary column */
  stats: ChartStats;
}

/**
 * Full response from GET /runs/{run_id}/chart-explanations.
 * Contains explanations for every chart produced by the Artist Agent,
 * plus the Analyst Agent's key insights for the dataset.
 */
export interface ChartExplanationsResponse {
  timestamp: string;
  total_charts: number;
  /** High-level dataset insights from the Analyst Agent */
  key_insights: string[];
  /** One entry per chart in the same order as viz_summary.json */
  explanations: ChartExplanation[];
}

